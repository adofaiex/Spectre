    internal static string GetLangCode(SystemLanguage lang)
    {
        return langlist.TryGetValue(lang, out var code) ? code : "en";
    }

    internal static SystemLanguage CodeToLang(string code)
    {
        return langCodeToLanguage.TryGetValue(code, out var lang) ? lang : (SystemLanguage)10;
    }

    internal static SystemLanguage CodeToGoogleDocCode(string code)
    {
        return langCodeToGoogleDocCode.TryGetValue(code, out var lang) ? lang : (SystemLanguage)10;
    }

    internal static void GetLanguages()
    {
        if (langlist.TryGetValue(RDString.language, out var code))
            currentLanguage = code;
    }

    internal static string GetLocalizedText(string key, string lang = null)
    {
        lang ??= currentLanguage;
        if (languageData.TryGetValue(lang, out var dict) && dict.TryGetValue(key, out var val))
            return val;
        if (lang != "en" && languageData.TryGetValue("en", out var enDict) && enDict.TryGetValue(key, out var enVal))
            return enVal;
        return key;
    }

    internal static void SwitchLanguage(string langCode)
    {
        if (languageData.ContainsKey(langCode))
        {
            currentLanguage = langCode;
            Debug.Log(("Language switched to " + langCode + "."));
        }
    }
